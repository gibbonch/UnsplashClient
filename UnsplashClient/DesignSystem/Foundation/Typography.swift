import UIKit

enum Typography {
    static let titleLarge = UIFont.systemFont(ofSize: 17, weight: .semibold)
    static let titleMedium = UIFont.systemFont(ofSize: 16, weight: .semibold)
    
    static let bodyLarge = UIFont.systemFont(ofSize: 17, weight: .regular)
    static let bodyMedium = UIFont.systemFont(ofSize: 16, weight: .medium)
    static let bodySmall = UIFont.systemFont(ofSize: 16, weight: .regular)
    
    static let caption = UIFont.systemFont(ofSize: 12, weight: .regular)
    static let captionSmall = UIFont.systemFont(ofSize: 11, weight: .regular)
}
